package group;

public class InvalidPermutationException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4955758047612922413L;

	public InvalidPermutationException() {
		super();
	}

	public InvalidPermutationException(String arg0) {
		super(arg0);
	}

	public InvalidPermutationException(Throwable arg0) {
		super(arg0);
	}

	public InvalidPermutationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidPermutationException(String arg0, Throwable arg1,
			boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
